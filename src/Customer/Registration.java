    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        String d = new String(RegDate.getText()); 
        try {
            
                    
            String sql = "INSERT INTO Details (Name, Adress, Phoneno, NICNO, ServiceType, CustomerID, Date)values (?,?,?,?,?,?,?)";
            pst = con.prepareStatement(sql);
            pst.setString(1, Name.getText());
            pst.setString(2, Adress.getText());
            pst.setInt(3, Integer.parseInt(Phoneno.getText()));
            pst.setString(4, NICNO.getText());
            pst.setString(5, ServiceType.getSelectedItem().toString());
            pst.setInt(6, Integer.parseInt(CustomerID.getText()));
            pst.setString(7, d);

            pst.execute();
            JOptionPane.showMessageDialog(null, "saved");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }

        try {
            String sql = "INSERT INTO Status (Name, Customer_ID, Status)values (?,?,?)";
            pst = con.prepareStatement(sql);
            pst.setString(1, Name.getText());
            pst.setInt(2, Integer.parseInt(CustomerID.getText()));
            pst.setString(3, Status.getText());

            pst.execute();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        try {
            String sql = "INSERT INTO Loayalty (Name, Customer_ID, Date)values (?,?,?)";
            pst = con.prepareStatement(sql);
            pst.setString(1, Name.getText());
            pst.setInt(2, Integer.parseInt(CustomerID.getText()));
            pst.setString(3, d);

            pst.execute();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        clearFields();
    }//GEN-LAST:event_saveActionPerformed

