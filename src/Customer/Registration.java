    private void PhonenoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PhonenoKeyTyped
        char c = evt.getKeyChar();
        if (!((Character.isDigit(c)))) {
            getToolkit().beep();
            evt.consume();
        }
        
        if ((Phoneno.getText() + evt.getKeyChar()).length() > 10) {
        evt.consume();
    }


    }//GEN-LAST:event_PhonenoKeyTyped

    private void NameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NameKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!(((Character.isLetter(c)))|| (c==KeyEvent.VK_BACKSPACE)
            || (c==KeyEvent.VK_DELETE) || (c==KeyEvent.VK_SPACE) )) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_NameKeyTyped

